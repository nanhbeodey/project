package model;

/**
 *
 * @author Nanhbeo
 */
public class HistorySearch {

    int id;
    String keyword;
    int uid;
    int type;

    public HistorySearch(int id, String keyword, int uid, int type) {
        this.id = id;
        this.keyword = keyword;
        this.uid = uid;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

}
