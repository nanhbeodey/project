package model;

import java.sql.Date;

/**
 *
 * @author Nanhbeo
 */

public class User {

    int id;
    String email;
    String fullname;
    boolean gender;
    String phone;
    String password;
    Date dob;
    Date createDate;
    String address;

    public User() {
    }

    public User(int id, String email, String fullname, boolean gender, String phone, String password, Date dob, Date createDate, String address) {
        this.id = id;
        this.email = email;
        this.fullname = fullname;
        this.gender = gender;
        this.phone = phone;
        this.password = password;
        this.dob = dob;
        this.createDate = createDate;
        this.address = address;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
    
}
