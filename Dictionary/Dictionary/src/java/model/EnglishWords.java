package model;

/**
 *
 * @author Nanhbeo
 */
public class EnglishWords {

    int id;
    String word;
    String definition;
    String exp;

    public EnglishWords(int id, String word, String definition, String exp) {
        this.id = id;
        this.word = word;
        this.definition = definition;
        this.exp = exp;
    }

    public String getExp() {
        return exp;
    }

    public void setExp(String exp) {
        this.exp = exp;
    }

    public EnglishWords(int id, String word, String definition) {
        this.id = id;
        this.word = word;
        this.definition = definition;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public String getDefinition() {
        return definition;
    }

    public void setDefinition(String definition) {
        this.definition = definition;
    }

}
